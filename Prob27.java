//  Write a program to generate a multiplication table for a given number.

public class Prob27 {

    public static void main(String[] args) {
        int num = 5;
        int i = 1;
        while (i <= 10) {
            System.out.println("5" + "*" + i + "*" + "=" + num * i);
            i++;
        }
    }

}
